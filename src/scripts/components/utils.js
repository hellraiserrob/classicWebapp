/**
 * simple add
 * @param {*} a 
 * @param {*} b 
 */

export function add(a, b) {
    return a + b;
}

/**
 * simple subtract
 * @param {*} a 
 * @param {*} b 
 */
export function subtract(a, b){
    return a - b;
}

/**
 * merge 2 objects
 * @param {*} a 
 * @param {*} b 
 */
export function merge(a, b){
    return Object.assign(a, b);
}